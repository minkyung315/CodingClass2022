print("안녕하세요"[0]) #첫번째 문자를 선택하는 연산자
print("안녕하세요"[1]) #두번째 문자를 선택하는 연산자
print("안녕하세요"[2]) #세번째 문자를 선택하는 연산자
print("안녕하세요"[3]) #네번째 문자를 선택하는 연산자
print("안녕하세요"[4]) #다섯번째 문자를 선택하는 연산자
print("\n") #줄바꿈
print("안녕하세요"[1:3]) #두번째 문자부터 네번째 문자 앞까지
print("안녕하세요"[2:4]) #세번째 문자부터 다섯번째 문자 앞까지
print("안녕하세요"[1:]) #두번째 문자부터 끝까지
print("안녕하세요"[:3]) #처음부터 4번째 문자 앞까지